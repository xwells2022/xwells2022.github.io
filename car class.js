/**
 * Class used to store car info
 */
class Car {
    constructor(imgSRC, type, name, transmission, people, bags, price) {
        this.imgSRC = imgSRC;
        this.type = type;
        this.name = name;
        this.transmission = transmission;
        this.people = people;
        this.bags = bags;
        this.price = price;
    }
}